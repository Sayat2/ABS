        <h3>What is React?</h3>
        <p>To cut a long story short, React is a JavaScript library for building User Interfaces. Very often it’s misinterpreted as a tool, framework, language. <a href="https://brainhub.eu/library/top-react-development-companies"> Vendors working with React</a> are able to create large web applications which use data that can change over time, without reloading the page. Its main goal is to be fast, simple and scalable. React processes only user interface in applications. It is simply a JavaScript runtime. <b> Lightweight, fast and modern</b> way to execute code on your computer.</p>
        
        <br><br>
        <h3>Why do internet giants choose React apps?</h3>
        <p><b>#1 Facebook</b></p>
        <p>Although partially, Facebook is making use of ReactJS. Their webpage is built with React, as the script that is blended in the application code. The mobile app is also built with a version of React called React Native which is similar, although responsible for displaying the iOS and Android native components instead of the DOM elements. </p>
        <p>Interestingly, Facebook was the place where the ReactJS library was initially created, which is why it is obvious for the app to use it. Facebook currently opened a beta of its completely rewritten ReactJS, called React Fiber.</p>
