        <h3>Building Blocks of a Web Application</h3>
        <p>There are a few things you need to consider when building modern applications. Such as: </p>
    
        <li><b>User Interface</b> - how users will consume and interact with your application.</li>
        <li><b>Routing</b> - how users navigate between different parts of your application.</li>
        <li><b>Data Fetching</b> - where your data lives and how to get it.</li>
        <li><b>Rendering</b> - when and where you render static or dynamic content.</li>
        <li><b>Integrations</b> - what third-party services you use (CMS, auth, payments, etc) and how you connect to them.</li>
        <li><b>Infrastructure</b> - where you deploy, store, and run your application code (Serverless, CDN, Edge, etc).</li>
        <li><b>Performance</b> - how to optimize your application for end-users.</li>
        <li><b>Scalability</b> - how your application adapts as your team, data, and traffic grow.</li>
        <li><b>Developer Experience</b> - your team's experience building and maintaining your application.</li>
    
        <p> For each part of your application, you will need to decide whether you will build a solution yourself or use other tools such as libraries and frameworks.</p>
