        <h3>What is HTML?</h3>
        <p>Let's start by breaking down exactly what the acronym 'HTML' stands for. HTML stands for HyperText Markup Language. HyperText is effectively a text within a text, and a markup language is a language understood by computers, designed to essentially describe webpages and make the text you use more interactive.</p>
        <p> It was originally invented back in 1989 as the publishing language of the web, and to put it very simply, it is the first building block in creating a webpage. When you first come across the world of HTML, you'll likely encounter a whole ream of terms and phrases that are somewhat baffling too. The three main ones are elements, tags, and attributes.  </p>
        <p>HTML5 is the latest 'version' of HTML, and knowledge in this area is a growing trend in the job market. By and large, it has the same functionality as standard HTML but is much more dynamic and uses much less code to build something fantastic. </p>
       <br>  <br>
        <h3>What is HTML used for?</h3>
        <p>In essence, HTML is used for creating the primary content of a webpage, giving it structure. You start by writing words, then apply tags or elements to these words. The web browser then reads this and can then understand the heading of a page, any paragraphs, and where the page starts and finishes, thus filling your web page with content. </p>
        <p>HTML is supported by every single browser and is established on pretty much every webpage in existence. You don't need any licenses, you don't need to pay for it, and it can be pretty easy to learn and code. If we can compare a webpage to the human body, then HTML is the bones of the body.</p>
