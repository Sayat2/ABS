        <h3>What is CSS? </h3>
        <p>CSS is the acronym for Cascade Styling Sheets. In short, it is a sheet style language, which is a type of language you can use to describe the presentation of a markup language - in this case, to describe the movements of HTML. It effectively determines how the building blocks, as laid by HTML, are decorated and presented to the user.</p>
        <p> CSS was first created around 1996 to make sense of HTML and make the webpage look and feel amazing. Just as with any language, you have to write CSS, and knowing how we write CSS is a skill that any webpage developer needs to have. Easy to maintain, CSS is the second part of the two-part tool kit for webpage creation.     </p>
        <p>Web accessibility is really important to many employers and companies nowadays, and there is fierce competition in creating the most accessible and well-designed webpage. Learning some software development fundamentals involving CSS can help you learn how to create accessible web pages.</p>
       <br>  <br>
        <h3>What is CSS used for? </h3>
        <p>If HTML is the bones of the body, then CSS is the skin that covers it. It's used for background colour, styling, layout, borders, shadowing - all the essential design bits and bobs that make a webpage look slick and smart. CSS enables you to distinguish between presentation and content by modifying the design and display of HTML elements.</p>
        <p>Presentation and ease of use are a couple of the main things that CSS has bought to web design by translating the way content looks on a webpage and what else goes on it to complement that content. While frequently used in conjunction with HTML, it is actually independent of it, and can be used with any XML-based markup language. </p>
